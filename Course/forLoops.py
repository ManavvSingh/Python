# For loop
string = "Hello world"
for i in string:
    print(i)


# Nested loop
myDict = ['orange','apple','kiwi']
for i in myDict:
    print(i)
    for x in i:
        print(x)

# Range function

for i in range(5):
    print(i)

for i in range(2,5):
    print(i)

for i in range(0,20,4):
    print(i)

